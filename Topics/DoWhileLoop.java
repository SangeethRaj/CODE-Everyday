package Topics;
public class DoWhileLoop 
{
    public static void main (String[] args)
    {
        int i = 1;

        do
        {
            System.out.println("The value is "+i);
            i = i + 1;
        } 
        while (i <= 5);

        int a = 10;

        do
        {
            System.out.println("The value is "+a);
            i = i + 1;
        }
        while (i < 5 );
    }   
}
