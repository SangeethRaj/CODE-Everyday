package Topics;
public class ForLoop
{
    public static void main (String[] args)
        {
            for (int i = 1; i <=5; i++)
            {
                System.out.println("The value is "+i);
            }

            for ( int a =10; a>=1; a--)
            {
                System.out.println("The value is "+a);
            }
        }
}